    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::find($id);

        if(is_null($user)){
            return response()->json(['message' => 'user not found'], 404);
        }else{
            return response()->json($user, 200);
        }
    }

